export const SC_PRICE = 13;

export const ITEM_MAP = {};
export const CAT_MAP = {};
MENU.forEach(cat => cat.items.forEach(item => {
  ITEM_MAP[item.id] = item;
  CAT_MAP[item.id] = cat.id;
}));

export const addCustomMenuItem = (newItem, categoryId) => {
  let cat = MENU.find(c => c.id === categoryId || c.label.toLowerCase() === categoryId.toLowerCase());
  
  if (!cat) {
    cat = {
      id: categoryId.toLowerCase().replace(/[^a-z0-9]/g, '_'),
      label: categoryId,
      emoji: "✨",
      items: []
    };
    MENU.push(cat);
  }

  cat.items.push(newItem);
  ITEM_MAP[newItem.id] = newItem;
  CAT_MAP[newItem.id] = cat.id;
};

export const updateMenuItemRate = (itemId, newRate) => {
  if (ITEM_MAP[itemId]) {
    ITEM_MAP[itemId].price = parseFloat(newRate);
  }
};

export const deleteMenuItem = (itemId) => {
  for (let cat of MENU) {
    const idx = cat.items.findIndex(i => i.id === itemId);
    if (idx !== -1) {
      cat.items.splice(idx, 1);
      break;
    }
  }
  delete ITEM_MAP[itemId];
  delete CAT_MAP[itemId];
};
